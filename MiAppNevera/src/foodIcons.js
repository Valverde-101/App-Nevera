let defaultFoodsMap = {};
let customFoodsMap = {};

export function setDefaultFoodsMap(overrides) {
  defaultFoodsMap = Object.fromEntries(overrides.map(o => [o.key, o]));
}

export function setCustomFoodsMap(customFoods) {
  customFoodsMap = Object.fromEntries(customFoods.map(f => [f.key, f]));
}

export function normalizeFoodName(name) {
  return name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').replace(/[^a-z0-9]/g, '');
}

export function getFoodInfo(name) {
  const key = normalizeFoodName(name);
  const base = foodData[key] || customFoodsMap[key];
  const override = defaultFoodsMap[key];
  return base || override ? { ...base, ...override } : base;
}

export function getFoodIcon(name) {
  const info = getFoodInfo(name);
  if (!info) return undefined;
  if (info.icon) {
    return typeof info.icon === 'string' ? { uri: info.icon } : info.icon;
  }
  if (info.baseIcon) {
    return foodIcons[info.baseIcon];
  }
  return undefined;
}

export function getFoodCategory(name) {
  const normalized = normalizeFoodName(name);
  for (const [cat, data] of Object.entries(categories)) {
    if (data.items.includes(normalized)) {
      return cat;
    }
  }
  return customFoodsMap[normalized]?.category || null;
}

